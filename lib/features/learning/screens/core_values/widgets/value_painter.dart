class PetalWidget extends StatelessWidget {
  final List<Path> paths;
  final Size size;
  final void Function(int) onPetalTap;

  PetalWidget({required this.paths, required this.size, required this.onPetalTap});

  @override
  Widget build(BuildContext context) {
    return Stack(
      children: [
        // CustomPaint(
        //   size: size,
        //   painter: RPSCustomValuePainter(),
        // ),
        ...List.generate(paths.length, (index) {
          return GestureDetector(
            onTap: () => onPetalTap(index),
            child: CustomPaint(
              size: size,
              painter: RPSCustomValuePainter(),
            ),
          );
        }),
      ],
    );
  }
}

class PetalPainter extends CustomPainter {
  final Path path;
  PetalPainter(this.path);

  @override
  void paint(Canvas canvas, Size size) {
    Paint paint = Paint()
      ..style = PaintingStyle.stroke
      ..color = Colors.transparent;
    canvas.drawPath(path, paint);
  }

  @override
  bool shouldRepaint(covariant CustomPainter oldDelegate) {
    return false;
  }
}